const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const functionsRoot = path.join(repoRoot, 'functions', 'api');
const outputFile = path.join(repoRoot, 'src', 'app', 'shared', 'service', 'functions.routes.generated.d.ts');

function walk(dir, files = []) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const fullPath = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            walk(fullPath, files);
        } else if (entry.isFile() && entry.name.endsWith('.ts')) {
            files.push(fullPath);
        }
    }
    return files;
}

function toRoute(filePath) {
    const relative = path.relative(functionsRoot, filePath).replace(/\\/g, '/');
    return relative.slice(0, -3);
}

function toTypeName(fileText, filePath) {
    if (!fileText.includes('export const onRequest')) return null;
    const match = fileText.match(/export type (\w+Function) = typeof onRequest;/);
    if (!match) throw new Error(`Missing endpoint export type in ${path.relative(repoRoot, filePath)}`);
    return match[1];
}

const entries = walk(functionsRoot)
    .map(filePath => {
        const fileText = fs.readFileSync(filePath, 'utf8');
        const typeName = toTypeName(fileText, filePath);
        if (!typeName) return null;
        return {
            route: toRoute(filePath),
            typeName,
            importPath: `@root/functions/api/${toRoute(filePath)}`,
        };
    })
    .filter(Boolean)
    .sort((a, b) => a.route.localeCompare(b.route));

const imports = entries
    .map(({ typeName, importPath }) => `import type { ${typeName} } from '${importPath}';`)
    .join('\n');

const mapEntries = entries
    .map(({ route, typeName }) => `    '${route}': ${typeName};`)
    .join('\n');

const output = `/* eslint-disable */\n/* This file is generated by tools/generate-function-route-map.js. Do not edit by hand. */\n\nimport type { Endpoint } from '@root/functions/shared/functions-utils';\n${imports}\n\ntype EndpointParams<TFunction> = TFunction extends Endpoint<infer P, any> ? P : never;\ntype EndpointResult<TFunction> = TFunction extends Endpoint<any, infer R> ? R : never;\n\nexport type FunctionRouteMap = {\n${mapEntries}\n};\n\nexport type FunctionRoute = keyof FunctionRouteMap;\nexport type FunctionParams<TRoute extends FunctionRoute> = EndpointParams<FunctionRouteMap[TRoute]>;\nexport type FunctionResult<TRoute extends FunctionRoute> = EndpointResult<FunctionRouteMap[TRoute]>;\n`;

fs.writeFileSync(outputFile, output, 'utf8');
console.log(`Generated ${path.relative(repoRoot, outputFile)}`);